package lecture;

public class Type {
	public static void main(String[] args) {
		// 기본 자료형, 원시 자료형, primitive type : 비 객체형 자료형
		// 1 Byte
		boolean bo = true;	
		byte by = 127;
		
		// 2 Byte
		char ch = 65;
		short sh = 32767;
		
		// 4 Byte
		int num = 10;
		float fl = 1.2F;
		
		// 8 Byte
		long ln = 100000000000L;
		double db = 3.1415926535;
	}
}


